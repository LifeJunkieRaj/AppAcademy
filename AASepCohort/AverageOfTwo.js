let averageOfTwo = function (num1, num2) {
    let sum = num1 + num2  // We have to define the variable sum
    let average = sum / 2  // We have to define the variable for average
    return average;  // We have to to return the function of this method with arguments assigned
}

console.log(averageOfTwo(3, 7)); // => 5.0
console.log(averageOfTwo(16, 5)); // => 10.5
console.log(averageOfTwo(2, 7.5)); // => 4.75
